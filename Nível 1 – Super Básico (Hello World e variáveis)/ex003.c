//Crie um programa que declare 3 variáveis int (a, b, c), coloque valores nelas e imprima a soma dos três.
#include <stdio.h>
int main() {
    int a = 10; // Primeiro valor
    int b = 20; // Segundo valor
    int c = 30; // Terceiro valor

    int soma = a + b + c; // Calcula a soma dos três valores

    printf("A soma de %d, %d e %d é: %d\n", a, b, c, soma); // Imprime o resultado

    return 0;
}